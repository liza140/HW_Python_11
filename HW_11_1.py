import matplotlib.pyplot as mat

a = 5
b =  10
c = -30
x = list(i for i in range(-6, 5))
F_x = []
for i in range(len(x)):
    F_x.append(a*(x[i]*x[i]) + b*x[i] + c)

mat.plot(x, F_x, "r-")
mat.show()

